package uppgift_3_new;

public class DateAndTime {

	private String dateTime;
	private String time;

	DateAndTime(String dateTime, String time) {
		this.dateTime = dateTime;
		this.time = time;
	}

	public String getDateTime() {
		return dateTime;
	}

	public String getTime() {
		return time;
	}

}
